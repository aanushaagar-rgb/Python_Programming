#Arithmetic Operators
a = 10
b = 2

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)#Modulo(Remainder)
print(a ** b)#For calculating power (a^b)

#Relational Operators/Comparison Operators
#Always gives a boolean value(True or False)
a = 10
b = 10
print(a == b)#equal too
print(a != b)#not equal too
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

#Arithmetic Operators
