#Logical Operators(not , and ,or)
a = 40
b = 50
print(not (a>b))#if condition is false & we write not before it, returns true
print(not (a<b))#if condition is true & we write not before it, returns false

value1 = True
value2 = False
print("AND operator:", value1 and value2)
print("OR operator:", value1 or value2)


